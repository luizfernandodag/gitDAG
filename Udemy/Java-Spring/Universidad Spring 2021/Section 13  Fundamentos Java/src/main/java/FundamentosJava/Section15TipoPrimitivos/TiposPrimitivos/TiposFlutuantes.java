package FundamentosJava.Section15TipoPrimitivos.TiposPrimitivos;

public class TiposFlutuantes {

    public static void main(String[] args) {
        //float
        float f = (float)1.2;
        System.out.println("float f: "+f);

        System.out.println("Tamanho de um float ( Quantidade de bits): " + Float.SIZE);
        System.out.println("Quantidade de bytes do tipo float:  "+ Float.BYTES);
        System.out.println("Valor maximo do tipo float: "+ Float.MAX_VALUE);
        System.out.println("Valor minimo do tipo float " + Float.MIN_VALUE);
        //double

        System.out.println("Tamanho de um double ( Quantidade de bits): " + Double.SIZE);
        System.out.println("Quantidade de bytes do tipo float:  "+ Double.BYTES);
        System.out.println("Valor maximo do tipo double: "+ Double.MAX_VALUE);
        System.out.println("Valor minimo do tipo double: " + Double.MIN_VALUE);
        double d = -1500000000;
        System.out.println("valor variavel d: " +d);






    }
}
