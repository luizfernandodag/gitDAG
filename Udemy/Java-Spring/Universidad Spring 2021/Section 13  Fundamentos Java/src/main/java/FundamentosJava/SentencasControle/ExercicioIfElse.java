package FundamentosJava.SentencasControle;

public class ExercicioIfElse {
    public static void main(String[] args) {
        int num = 1;
        String resultado="";

        if(num ==1 )
        {
            resultado = "numero = 1";
        }
        else if(num == 2)
        {
            resultado = "numero = 2";
        }
        else if (num == 3 )
        {
            resultado = "numero = 2";
        }
        else
        {
            resultado = "numero indeterminado";
        }
        System.out.println(resultado);


    }
}
