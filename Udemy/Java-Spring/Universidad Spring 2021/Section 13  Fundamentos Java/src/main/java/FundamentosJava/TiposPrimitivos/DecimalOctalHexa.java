package FundamentosJava.TiposPrimitivos;

public class DecimalOctalHexa {
    public static void main(String[] args) {
        int numeroDecimal = 34;
        int numeroOctal = 034;
        int numeroHexaDecimal = 0x34;
        int numeroBinario = 0b111;

        System.out.println("Numero decimal: "+ numeroDecimal);
        System.out.println("Numero octal: "+ numeroOctal);
        System.out.println("Numero hexadecimal: "+ numeroHexaDecimal);
        System.out.println("Numero binario: "+ numeroBinario);

    }
}
